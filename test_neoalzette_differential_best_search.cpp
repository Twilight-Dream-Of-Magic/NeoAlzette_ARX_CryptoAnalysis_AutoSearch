	static bool differential_result_has_trail( const BestSearchResult& result ) noexcept
	{
		return result.found && result.best_weight < INFINITE_WEIGHT && !result.best_trail.empty();
	}

	static bool differential_result_is_resumable_pause( const BestSearchResult& result ) noexcept
	{
		return !differential_result_has_trail( result ) && ( result.hit_maximum_search_nodes || result.hit_time_limit );
	}

	static int differential_exit_code_from_result( const BestSearchResult& result ) noexcept
	{
		return ( differential_result_has_trail( result ) || differential_result_is_resumable_pause( result ) ) ? 0 : 2;
	}

