	static bool differential_auto_candidate_better( const DifferentialAutoCandidate& a, const DifferentialAutoCandidate& b )
	{
		if ( a.best_weight != b.best_weight )
			return a.best_weight < b.best_weight;
		if ( a.entry_round1_weight != b.entry_round1_weight )
			return a.entry_round1_weight < b.entry_round1_weight;
		if ( a.start_delta_a != b.start_delta_a )
			return a.start_delta_a < b.start_delta_a;
		if ( a.start_delta_b != b.start_delta_b )
			return a.start_delta_b < b.start_delta_b;
		return a.nodes < b.nodes;
	}

	static void differential_sort_auto_top_candidates( std::vector<DifferentialAutoCandidate>& top_candidates )
	{
		std::sort( top_candidates.begin(), top_candidates.end(), []( const DifferentialAutoCandidate& x, const DifferentialAutoCandidate& y ) {
			return differential_auto_candidate_better( x, y );
		} );
	}

