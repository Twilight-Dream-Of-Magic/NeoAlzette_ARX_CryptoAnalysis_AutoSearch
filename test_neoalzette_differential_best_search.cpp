	static bool read_differential_auto_pipeline_checkpoint( const std::string& path, DifferentialAutoPipelineState& state )
	{
		return TwilightDream::auto_pipeline_shared::read_checkpoint_file(
			path,
			TwilightDream::auto_search_checkpoint::SearchKind::DifferentialAutoPipeline,
			[ & ]( TwilightDream::auto_search_checkpoint::BinaryReader& r ) {
				TwilightDream::auto_pipeline_shared::AutoPipelinePayloadHeader header {};
				if ( !TwilightDream::auto_pipeline_shared::read_payload_header(
						 r,
						 state,
						 header,
						 [ & ]() { return read_config( r, state.search_configuration ); } ) )
					return false;
				if ( header.breadth_strategy != static_cast<std::uint8_t>( CommandLineOptions::AutoBreadthStrategy::FrontierBfs ) ) return false;
				state.start_difference_a = header.start_word_a;
				state.start_difference_b = header.start_word_b;
				state.auto_breadth_strategy = CommandLineOptions::AutoBreadthStrategy::FrontierBfs;
				state.stage = static_cast<DifferentialAutoPipelineStage>( header.stage );

				if ( !TwilightDream::auto_pipeline_shared::read_counted_container( r, state.jobs, [ & ]( DifferentialAutoBreadthJob& job ) {
						 return read_differential_auto_job( r, job );
					 } ) )
					return false;
				if ( !TwilightDream::auto_pipeline_shared::read_counted_container( r, state.completed_job_flags, [ & ]( std::uint8_t& flag ) {
						 return r.read_u8( flag );
					 } ) )
					return false;
				if ( !r.read_u64( state.breadth_total_nodes_visited ) ) return false;
				if ( !TwilightDream::auto_pipeline_shared::read_counted_container( r, state.top_candidates, [ & ]( DifferentialAutoCandidate& candidate ) {
						 return read_differential_auto_candidate( r, candidate );
					 } ) )
					return false;
				if ( !TwilightDream::auto_pipeline_shared::read_optional_section( r, state.has_selected_candidate, [ & ]() {
						 return read_differential_auto_candidate( r, state.selected_candidate );
					 } ) )
					return false;
				return TwilightDream::auto_pipeline_shared::read_optional_section( r, state.has_deep_snapshot, [ & ]() {
					return read_differential_checkpoint_payload( r, state.deep_snapshot, state.deep_memoization );
				} );
			} );
	}

