	static void differential_try_update_auto_top_candidates( std::vector<DifferentialAutoCandidate>& top_candidates, std::size_t top_k, DifferentialAutoCandidate&& candidate )
	{
		if ( !candidate.found || top_k == 0 )
			return;
		if ( top_candidates.size() < top_k )
		{
			top_candidates.push_back( std::move( candidate ) );
			differential_sort_auto_top_candidates( top_candidates );
			return;
		}
		std::size_t worst = 0;
		for ( std::size_t i = 1; i < top_candidates.size(); ++i )
		{
			if ( differential_auto_candidate_better( top_candidates[ worst ], top_candidates[ i ] ) )
				continue;
			worst = i;
		}
		if ( differential_auto_candidate_better( candidate, top_candidates[ worst ] ) )
		{
			top_candidates[ worst ] = std::move( candidate );
			differential_sort_auto_top_candidates( top_candidates );
		}
	}

	static void differential_update_auto_candidate_from_best_result( DifferentialAutoCandidate& candidate, const BestSearchResult& result )
	{
		candidate.found = differential_result_has_trail( result );
		candidate.nodes = result.nodes_visited;
		candidate.hit_maximum_search_nodes_limit = result.hit_maximum_search_nodes;
		if ( !candidate.found )
			return;
		candidate.best_weight = result.best_weight;
		if ( !result.best_trail.empty() )
		{
			candidate.entry_round1_weight = result.best_trail.front().round_weight;
			candidate.entry_delta_a = result.best_trail.front().output_branch_a_difference;
			candidate.entry_delta_b = result.best_trail.front().output_branch_b_difference;
		}
		candidate.trail = result.best_trail;
	}

	static void differential_apply_deep_result_to_auto_candidates(
		std::vector<DifferentialAutoCandidate>& top_candidates,
		DifferentialAutoCandidate& selected_candidate,
		const BestSearchResult& result )
	{
		const std::uint32_t start_delta_a = selected_candidate.start_delta_a;
		const std::uint32_t start_delta_b = selected_candidate.start_delta_b;
		differential_update_auto_candidate_from_best_result( selected_candidate, result );

		bool updated_existing = false;
		for ( auto& candidate : top_candidates )
		{
			if ( candidate.start_delta_a != start_delta_a || candidate.start_delta_b != start_delta_b )
				continue;
			differential_update_auto_candidate_from_best_result( candidate, result );
			updated_existing = true;
			break;
		}
		if ( !updated_existing )
			top_candidates.push_back( selected_candidate );
		differential_sort_auto_top_candidates( top_candidates );
	}

	static void write_differential_auto_job( TwilightDream::auto_search_checkpoint::BinaryWriter& w, const DifferentialAutoBreadthJob& job )
	{
		w.write_u32( job.delta_a );
		w.write_u32( job.delta_b );
	}

	static bool read_differential_auto_job( TwilightDream::auto_search_checkpoint::BinaryReader& r, DifferentialAutoBreadthJob& job )
	{
		return r.read_u32( job.delta_a ) && r.read_u32( job.delta_b );
	}

	static void write_differential_auto_candidate( TwilightDream::auto_search_checkpoint::BinaryWriter& w, const DifferentialAutoCandidate& candidate )
	{
		w.write_u32( candidate.start_delta_a );
		w.write_u32( candidate.start_delta_b );
		w.write_u8( candidate.found ? 1u : 0u );
		w.write_u64( candidate.entry_round1_weight );
		w.write_u64( candidate.best_weight );
		w.write_u64( candidate.nodes );
		w.write_u8( candidate.hit_maximum_search_nodes_limit ? 1u : 0u );
		w.write_u32( candidate.entry_delta_a );
		w.write_u32( candidate.entry_delta_b );
		write_trail_vector( w, candidate.trail );
	}

	static bool read_differential_auto_candidate( TwilightDream::auto_search_checkpoint::BinaryReader& r, DifferentialAutoCandidate& candidate )
	{
		std::uint8_t found = 0;
		std::uint8_t hit_node_limit = 0;
		if ( !r.read_u32( candidate.start_delta_a ) ) return false;
		if ( !r.read_u32( candidate.start_delta_b ) ) return false;
		if ( !r.read_u8( found ) ) return false;
		if ( !r.read_u64( candidate.entry_round1_weight ) ) return false;
		if ( !r.read_u64( candidate.best_weight ) ) return false;
		if ( !r.read_u64( candidate.nodes ) ) return false;
		if ( !r.read_u8( hit_node_limit ) ) return false;
		if ( !r.read_u32( candidate.entry_delta_a ) ) return false;
		if ( !r.read_u32( candidate.entry_delta_b ) ) return false;
		if ( !read_trail_vector( r, candidate.trail ) ) return false;
		candidate.found = ( found != 0 );
		candidate.hit_maximum_search_nodes_limit = ( hit_node_limit != 0 );
		return true;
	}

	static bool write_differential_auto_pipeline_checkpoint_payload(
		TwilightDream::auto_search_checkpoint::BinaryWriter& w,
		const DifferentialAutoPipelineState& state,
		const DifferentialBestSearchContext* deep_context = nullptr,
		const DifferentialSearchCursor* deep_cursor = nullptr )
	{
		if ( !TwilightDream::auto_pipeline_shared::write_payload_header(
				 w,
				 state,
				 state.start_difference_a,
				 state.start_difference_b,
				 static_cast<std::uint8_t>( state.auto_breadth_strategy ),
				 static_cast<std::uint8_t>( state.stage ),
				 [ & ] { write_config( w, state.search_configuration ); } ) )
			return false;
		if ( !TwilightDream::auto_pipeline_shared::write_counted_container( w, state.jobs, [ & ]( const DifferentialAutoBreadthJob& job ) {
				 write_differential_auto_job( w, job );
			 } ) )
			return false;
		if ( !TwilightDream::auto_pipeline_shared::write_counted_container( w, state.completed_job_flags, [ & ]( std::uint8_t flag ) {
				 w.write_u8( flag );
			 } ) )
			return false;
		w.write_u64( state.breadth_total_nodes_visited );
		if ( !TwilightDream::auto_pipeline_shared::write_counted_container( w, state.top_candidates, [ & ]( const DifferentialAutoCandidate& candidate ) {
				 write_differential_auto_candidate( w, candidate );
			 } ) )
			return false;
		if ( !TwilightDream::auto_pipeline_shared::write_optional_section( w, state.has_selected_candidate, [ & ]() {
				 write_differential_auto_candidate( w, state.selected_candidate );
				 return w.ok();
			 } ) )
			return false;
		const bool write_live_deep_snapshot = ( deep_context != nullptr && deep_cursor != nullptr );
		return TwilightDream::auto_pipeline_shared::write_optional_section( w, write_live_deep_snapshot || state.has_deep_snapshot, [ & ]() {
			if ( write_live_deep_snapshot )
				return write_differential_checkpoint_payload( w, *deep_context, *deep_cursor, TwilightDream::best_search_shared_core::accumulated_elapsed_microseconds( *deep_context ) );
			DifferentialBestSearchContext snapshot_context {};
			snapshot_context.configuration = state.deep_snapshot.configuration;
			snapshot_context.start_difference_a = state.deep_snapshot.start_difference_a;
			snapshot_context.start_difference_b = state.deep_snapshot.start_difference_b;
			snapshot_context.history_log_output_path = state.deep_snapshot.history_log_path;
			snapshot_context.runtime_log_output_path = state.deep_snapshot.runtime_log_path;
			snapshot_context.accumulated_elapsed_usec = state.deep_snapshot.accumulated_elapsed_usec;
			snapshot_context.visited_node_count = state.deep_snapshot.total_nodes_visited;
			snapshot_context.run_visited_node_count = state.deep_snapshot.run_nodes_visited;
			snapshot_context.best_total_weight = state.deep_snapshot.best_total_weight;
			snapshot_context.best_differential_trail = state.deep_snapshot.best_trail;
			snapshot_context.current_differential_trail = state.deep_snapshot.current_trail;
			snapshot_context.runtime_controls.maximum_search_nodes = state.deep_snapshot.runtime_maximum_search_nodes;
			snapshot_context.runtime_controls.maximum_search_seconds = state.deep_snapshot.runtime_maximum_search_seconds;
			snapshot_context.runtime_controls.progress_every_seconds = state.deep_snapshot.runtime_progress_every_seconds;
			snapshot_context.runtime_controls.checkpoint_every_seconds = state.deep_snapshot.runtime_checkpoint_every_seconds;
			snapshot_context.runtime_state.total_nodes_visited = state.deep_snapshot.total_nodes_visited;
			snapshot_context.runtime_state.run_nodes_visited = state.deep_snapshot.run_nodes_visited;
			snapshot_context.runtime_state.progress_node_mask = state.deep_snapshot.runtime_progress_node_mask;
			snapshot_context.runtime_state.stop_due_to_node_limit = state.deep_snapshot.last_run_hit_node_limit;
			snapshot_context.runtime_state.stop_due_to_time_limit = state.deep_snapshot.last_run_hit_time_limit;
			return write_differential_checkpoint_payload( w, snapshot_context, state.deep_snapshot.cursor, state.deep_snapshot.accumulated_elapsed_usec );
		} );
	}

