	static inline std::string to_lowercase_ascii( std::string s )
	{
		for ( char& ch : s )
		{
			if ( ch >= 'A' && ch <= 'Z' )
				ch = char( ch - 'A' + 'a' );
		}
		return s;
	}

	static constexpr std::uint64_t k_mib = 1024ull * 1024ull;
	static constexpr std::uint64_t k_gib = 1024ull * 1024ull * 1024ull;

	static std::uint64_t saturating_add_u64( std::uint64_t a, std::uint64_t b ) noexcept
	{
		const std::uint64_t maxv = std::numeric_limits<std::uint64_t>::max();
		return ( a > maxv - b ) ? maxv : ( a + b );
	}

	static std::uint64_t saturating_mul_u64( std::uint64_t a, std::uint64_t b ) noexcept
	{
		const std::uint64_t maxv = std::numeric_limits<std::uint64_t>::max();
		if ( a == 0 || b == 0 )
			return 0;
		return ( a > maxv / b ) ? maxv : ( a * b );
	}

	static std::uint64_t mebibytes_to_bytes( std::uint64_t mib ) noexcept
	{
		return saturating_mul_u64( mib, k_mib );
	}

	static std::uint64_t estimate_differential_optional_rebuildable_bytes( const CommandLineOptions& command_line_options ) noexcept
	{
		std::uint64_t estimate = mebibytes_to_bytes( command_line_options.rebuildable_reserve_mib );
		if ( command_line_options.search_configuration.enable_weight_sliced_pddt )
			estimate = saturating_add_u64( estimate, 256ull * k_mib );
		return estimate;
	}

	static std::uint64_t estimate_differential_must_live_bytes(
		const CommandLineOptions& command_line_options,
		bool					  auto_mode,
		bool					  resume_mode ) noexcept
	{
		std::uint64_t estimate = 192ull * k_mib;
		estimate = saturating_add_u64( estimate, saturating_mul_u64( std::uint64_t( std::max( 1, command_line_options.round_count ) ), 64ull * k_mib ) );
		estimate = saturating_add_u64( estimate, command_line_options.search_configuration.enable_state_memoization ? ( 512ull * k_mib ) : ( 128ull * k_mib ) );
		estimate = saturating_add_u64( estimate, command_line_options.search_configuration.enable_weight_sliced_pddt ? ( 256ull * k_mib ) : ( 96ull * k_mib ) );
		const std::uint64_t cache_per_thread =
			command_line_options.cache_was_provided ? std::uint64_t( command_line_options.cache_max_entries_per_thread ) : 262144ull;
		estimate = saturating_add_u64( estimate, saturating_mul_u64( cache_per_thread, 32ull ) );
		estimate = saturating_add_u64( estimate, saturating_mul_u64( std::uint64_t( command_line_options.shared_cache_total_entries ), 32ull ) );
		if ( command_line_options.runtime_controls.maximum_search_seconds != 0 )
		{
			estimate = saturating_add_u64( estimate, 768ull * k_mib );
		}
		else if ( command_line_options.runtime_controls.maximum_search_nodes != 0 )
		{
			const std::uint64_t node_signal =
				std::min<std::uint64_t>(
					6ull * k_gib,
					saturating_mul_u64(
						std::max<std::uint64_t>( 1ull, command_line_options.runtime_controls.maximum_search_nodes / 250000ull ),
						192ull * k_mib ) );
			estimate = saturating_add_u64( estimate, node_signal );
		}
		if ( auto_mode )
		{
			const unsigned hardware_thread_concurrency = std::thread::hardware_concurrency();
			const int	   auto_threads_raw =
				( command_line_options.auto_breadth_thread_count > 0 ) ?
					command_line_options.auto_breadth_thread_count :
					int( ( hardware_thread_concurrency == 0 ) ? 1 : hardware_thread_concurrency );
			const std::uint64_t auto_threads = std::uint64_t( std::max( 1, auto_threads_raw ) );
			estimate = saturating_add_u64( estimate, 512ull * k_mib );
			estimate = saturating_add_u64( estimate, saturating_mul_u64( auto_threads, 64ull * k_mib ) );
			estimate = saturating_add_u64(
				estimate,
				std::min<std::uint64_t>(
					1ull * k_gib,
					saturating_mul_u64( std::uint64_t( std::max<std::size_t>( 1, command_line_options.auto_breadth_candidate_count ) ), 32ull * 1024ull ) ) );
		}
		if ( resume_mode )
			estimate = saturating_add_u64( estimate, 192ull * k_mib );
		return estimate;
	}

	static MemoryGateEvaluation evaluate_differential_memory_gate(
		const CommandLineOptions& command_line_options,
		const SystemMemoryInfo&   system_memory_info,
		bool					  auto_mode,
		bool					  resume_mode ) noexcept
	{
		return evaluate_memory_gate(
			system_memory_info.available_physical_bytes,
			estimate_differential_must_live_bytes( command_line_options, auto_mode, resume_mode ),
			estimate_differential_optional_rebuildable_bytes( command_line_options ) );
	}

